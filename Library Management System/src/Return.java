    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String sql="select Book_Name,Publisher,Price,Date_Of_Issue,Edition from Issue where Book_Id=? and Student_Id=?";
        try
        {
            pst=conn.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(jTextField2.getText()));
            pst.setInt(2, Integer.parseInt(jTextField1.getText()));
            rs=pst.executeQuery();
            
            if(rs.next())
            {
                jTextField3.setText(rs.getString(1));
                jTextField4.setText(rs.getString(2));
                jTextField5.setText(Integer.toString(rs.getInt(3)));
                jTextField6.setText(rs.getString(4));
                jTextField7.setText(rs.getString(5));
                
                rs.close();
                pst.close();
            }
            
            else
            {
                JOptionPane.showMessageDialog(rootPane, "INVALID BOOK_ID OR STUDENT ID");
            }
            
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        
        finally
        {
            try
            {
                pst.close();
                rs.close();
            }
            catch(SQLException e)
            {
                
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

