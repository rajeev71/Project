        // TODO add your handling code here:
        
        String sql="select name,course,branch ,profile_pic from student where student_id= ?";
        try
        {
            pst=conn.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(sid));
            rs=pst.executeQuery();
            
            if(rs.next())
              {
                 String s1=rs.getString(1);
                 String s2=rs.getString(2);
                 String s3=rs.getString(3);
                 byte[] s4=rs.getBytes(4);
                 
                 pst.close();
                 rs.close();
                  
                  setVisible(false);
                  new StudentActivity(s1,s2,s3,Integer.parseInt(sid),s4).setVisible(true);
                  
              } 
            
            

        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        
