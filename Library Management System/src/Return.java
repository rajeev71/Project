    public void  delete()
    {
        String sql="delete from issue where Student_Id=? and Book_Id=?";
        try
        {
            pst=conn.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(jTextField1.getText()));
            pst.setInt(2, Integer.parseInt(jTextField2.getText()));
            pst.execute();
        }
        
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    public void ReturnUpdate()
    {
        String sql="insert into return values(?,?,?,?,?,?,?,?)";
        try
        {
            pst=conn.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(jTextField1.getText()));
            pst.setInt(2, Integer.parseInt(jTextField2.getText()));
            pst.setString(3, (jTextField3.getText()));
            pst.setString(4,(jTextField4.getText()));
            pst.setInt(5, Integer.parseInt(jTextField5.getText()));
            pst.setString(6, (jTextField6.getText()));
            pst.setInt(7, Integer.parseInt(jTextField7.getText()));
            pst.setString(8, ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText());
            pst.execute();
            
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        
    }
    
